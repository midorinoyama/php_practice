<?php
//    ***1
//    **121
//    *12321
//    1234321

//y軸中央は1から4まで1ずつ増えている
for ($y = 1; $y <= 4; $y++) {
    //  *を表示するループ
    for ($x = 4; $x >= $y; $x--) {
        echo "*";
    }
    //  1~2,1~3と1ずつ増加まで
    for ($x = 1; $x < $y; $x++) {
        echo $x;
    }
    //
    for ($x = $y; $x >= 1; $x--) {
        echo $x;
    }
    echo "\n";
}
